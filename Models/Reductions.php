<?php



/**
 * Reductions
 */
class Reductions
{
    /**
     * @var integer
     */
    private $idReduction;

    /**
     * @var string
     */
    private $nom;

    /**
     * @var \DateTime
     */
    private $dateDebut;

    /**
     * @var \DateTime
     */
    private $dateFin;

    /**
     * @var integer
     */
    private $pourcentageReduction;


    /**
     * Get idReduction
     *
     * @return integer
     */
    public function getIdReduction()
    {
        return $this->idReduction;
    }

    /**
     * Set nom
     *
     * @param string $nom
     *
     * @return Reductions
     */
    public function setNom($nom)
    {
        $this->nom = $nom;

        return $this;
    }

    /**
     * Get nom
     *
     * @return string
     */
    public function getNom()
    {
        return $this->nom;
    }

    /**
     * Set dateDebut
     *
     * @param \DateTime $dateDebut
     *
     * @return Reductions
     */
    public function setDateDebut($dateDebut)
    {
        $this->dateDebut = $dateDebut;

        return $this;
    }

    /**
     * Get dateDebut
     *
     * @return \DateTime
     */
    public function getDateDebut()
    {
        return $this->dateDebut;
    }

    /**
     * Set dateFin
     *
     * @param \DateTime $dateFin
     *
     * @return Reductions
     */
    public function setDateFin($dateFin)
    {
        $this->dateFin = $dateFin;

        return $this;
    }

    /**
     * Get dateFin
     *
     * @return \DateTime
     */
    public function getDateFin()
    {
        return $this->dateFin;
    }

    /**
     * Set pourcentageReduction
     *
     * @param integer $pourcentageReduction
     *
     * @return Reductions
     */
    public function setPourcentageReduction($pourcentageReduction)
    {
        $this->pourcentageReduction = $pourcentageReduction;

        return $this;
    }

    /**
     * Get pourcentageReduction
     *
     * @return integer
     */
    public function getPourcentageReduction()
    {
        return $this->pourcentageReduction;
    }
}

